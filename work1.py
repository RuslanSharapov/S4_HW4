#  Вычислить число c заданной точностью d

#  Пример:

#  - при d = 0.001, π = 3.141    10 ^ {-1} ≤ d ≤ 10 ^ {-10}

#  Ряд Лейбница       X = 4 - 4/3 + 4/5 - 4/7 + 4/9 - ....

x = 1
summ = 0
for i in range(1000000):
    if i % 2 == 0:
        summ += 4 / x
    else:
        summ -= 4 / x
    x += 2
print(summ)

